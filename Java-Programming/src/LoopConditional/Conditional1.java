package LoopConditional;

public class Conditional1 {

	public static void main(String[] args) {

		// if ~ else
		String s = "I Love You";
		
		if(s.contains("Love")) {
			// Love를 포함하는 경우
			System.out.println("Me too!");
		} else {
			// Love를 포함하지 않는 경우
			System.out.println("I hate you!");
		}
		
	}

}
